<br><b>4. Segurança da Informação</b><br>
<br>4.1. Tomamos medidas de segurança técnicas e organizacionais adequadas para protegermos seus dados pessoais contra acesso, alteração, divulgação ou ainda destruição não autorizada dos dados. Essas medidas incluem análises internas de nossas práticas de coleta, armazenamento e processamento de dados e medidas de segurança, incluindo criptografia e medidas de segurança física apropriadas para nos proteger contra o acesso não autorizado a sistemas em que armazenamos os dados pessoais.<br>

<br>4.2. Nosso site utiliza uma tecnologia avançada de segurança. Todo o tráfego de dados é feito com as informações criptografadas, que é um método padrão usado na Internet para proteger as comunicações entre os usuários da Web e os sites, fornecendo uma navegação segura.<br>

<br>4.3. Limitamos o acesso às informações pessoais aos funcionários, contratantes e agentes do Buscgame que precisam ter conhecimento dessas informações para processá-las em nosso nome. Essas pessoas estão comprometidas com obrigações de confidencialidade e podem ser submetidas a punições, incluindo rescisão de contrato e processo criminal, caso não cumpram essas obrigações. O Buscgame não cadastra nenhum usuário sem sua devida permissão.<br>

