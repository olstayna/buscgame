<br><b>2. Cookies</b><br>
<br>2.1. No Buscgame, o uso de cookies é feito apenas para reconhecer um visitante constante e melhorar a experiência de navegação e usabilidade. Os cookies são pequenos arquivos de dados transferidos de um site da web para o disco do seu computador.<br>

<br><b>3. Compartilhamento de Dados Pessoais</b><br>
<br>3.1. O Buscgame pode compartilhar seus dados pessoais com empresas parceiras de negócio com o objetivo de fornecer a você as melhores ofertas de produtos e serviços, desenvolver e aprimorar sua experiência no Buscgame, inclusive para oferecer novos produtos e serviços ou em circunstâncias específicas como processos judiciais, investigações, ou advindas de lei, incluindo requisições de autoridades e órgãos governamentais.<br>

