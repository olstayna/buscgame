<br><b>A NAVEGAÇÃO E USO DESTE SITE SIGNIFICA QUE O USUÁRIO CONCORDA COM OS TERMOS E SERVIÇOS OFERECIDOS NESTA POLÍTICA DE PRIVACIDADE.<br></b>

<br><b>Tratamento dos dados pessoais</b><br>
<br><b>1. Coleta e Uso<br></b>
<br>1.1. Coletamos o mínimo necessário. Para seu cadastro, solicitamos informações como nome e e-mail. Utilizamos seu e-mail, apenas quando autorizado por você, para comunicar promoções de produtos e serviços.<br>

