<br>5.1. O Buscgame armazena seus dados pessoais pelo tempo exigido por lei ou pelo tempo que for necessário para preservar o legítimo interesse do Buscgame. Dessa forma, trataremos seus dados, por exemplo, durante os prazos prescricionais aplicáveis ou enquanto necessário para cumprimento de uma obrigação legal ou regulatória.<br>

<br>5.2. O término do tratamento de dados pessoais poderá ocorrer quando o Titular exercer seu direito de solicitar o fim do tratamento e a exclusão de seus dados pessoais ou quando houver determinação legal neste sentido.<br>

